# Baixar pacote ----------------------------------------------------------------------------------------------------------------------------

library(tidyverse)
library(dados)

# Baixar dados -----------------------------------------------------------------------------------------------------------------------------

dados <- dados::bebes
view(dados)

# Análises ---------------------------------------------------------------------------------------------------------------------------------

### função filter

dados_fem <- dados %>%
  filter(sexo == "F", ano == 1996)
view(dados_fem)  

dados_mas <- dados %>%
  filter(sexo == "M", ano == 1996)
view(dados_mas)
