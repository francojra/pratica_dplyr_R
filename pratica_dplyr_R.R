# Baixar pacote ----------------------------------------------------------------------------------------------------------------------------

library(tidyverse)
library(dados)

# Baixar dados -----------------------------------------------------------------------------------------------------------------------------

dados <- dados::bebes
view(dados)

# Análises ---------------------------------------------------------------------------------------------------------------------------------

### função filter

dados_fem <- dados %>%
  filter(sexo == "F", ano == 1996, n <= 20)
view(dados_fem)  

dados_mas <- dados %>%
  filter(sexo == "M", ano == 1996, n <= 20)
view(dados_mas)

### função select

dados_fem %>%
  select(nome, starts_with("An"))

dados_mas %>%
  select(nome, starts_with("Ja"))

